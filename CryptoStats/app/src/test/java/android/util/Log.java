package android.util;

/**
 * Created by Banty on 06/01/19.
 * <p>
 * This is created only to be used in unit testing to fix the RuntimeException which occurs while testing methods
 * containing Log class. Reference : https://stackoverflow.com/a/46793567/3827850
 */
public class Log {
    public static int d(String tag, String msg) {
        System.out.println("DEBUG: " + tag + ": " + msg);
        return 0;
    }

    public static int i(String tag, String msg) {
        System.out.println("INFO: " + tag + ": " + msg);
        return 0;
    }

    public static int w(String tag, String msg) {
        System.out.println("WARN: " + tag + ": " + msg);
        return 0;
    }

    public static int e(String tag, String msg) {
        System.out.println("ERROR: " + tag + ": " + msg);
        return 0;
    }
}